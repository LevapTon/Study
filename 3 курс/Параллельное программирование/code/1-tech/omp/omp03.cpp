#ifdef _OPENMP
#include <omp.h>
#endif //_OPENMP

#include <cstdlib>
#include "../../common/synprintf.hxx"
using namespace z;

int main(int argc, char *argv[])
{
    int n = (argc > 1) ? strtol(argv[1], NULL, 0) : 1;
    #pragma omp parallel if(n > 1 && n <= 16) num_threads(n)
    synprintf(stdout, "Hello, World!\n");
    return 0;
}
