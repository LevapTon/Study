import numpy as np

# Определение матрицы коэффициентов системы уравнений
A = np.array([[2, 1, -1],
              [1, 3, 2],
              [3, 2, -3]])

# Вектор правой части
b = np.array([4, 5, 6])

# Решение системы уравнений
x = np.linalg.solve(A, b)

print("-"*4,"ЗАДАНИЕ 3.2","-"*4)
print(x)
